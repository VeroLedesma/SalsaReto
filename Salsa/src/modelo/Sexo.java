package modelo;

/**
 * La enumeración Sexo representa los diferentes géneros.
 */
public enum Sexo {
    /**
     * Representa el género masculino.
     */
    MASCULINO,

    /**
     * Representa el género femenino.
     */
    FEMENINO,

    /**
     * Representa otros géneros diferentes a masculino y femenino.
     */
    OTROS;
}