package modelo;

/**
 * La enumeración Temporada representa las cuatro estaciones del año.
 * 
 * @autor Vero
 * @version 1.0
 */
public enum Temporada {
	/**
	 * La estación de la primavera.
	 */
	PRIMAVERA,

	/**
	 * La estación del verano.
	 */
	VERANO,

	/**
	 * La estación del otoño.
	 */
	OTONIO,

	/**
	 * La estación del invierno.
	 */
	INVIERNO;
}